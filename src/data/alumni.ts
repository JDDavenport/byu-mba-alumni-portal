export function getAlumniById(id: string): Alumni | undefined {
  return alumni.find((a) => a.id === id);
}

export function getUniqueIndustries(): string[] {
  return [...new Set(alumni.map((a) => a.industry))].sort();
}

export function getUniqueCompanies(): string[] {
  return [...new Set(alumni.map((a) => a.company))].sort();
}

export function getUniqueCities(): string[] {
  return [...new Set(alumni.map((a) => `${a.city}, ${a.state}`))].sort();
}

export function getGraduationYearRange(): { min: number; max: number } {
  const years = alumni.map((a) => a.graduationYear);
  return { min: Math.min(...years), max: Math.max(...years) };
}
